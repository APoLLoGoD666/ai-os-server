// Service → domain edges
function buildServiceEdges() {
    return SERVICES
        .filter(s => s.domain)
        .map(s => ({
            from:  s.id,
            to:    s.domain,
            type:  'belongs_to',
            label: `${s.name} belongs to domain`,
        }));
}

function isServiceNode(id) { return typeof id === 'string' && id.startsWith('SVC-'); }

module.exports = { SERVICES, buildServiceEdges, isServiceNode };
