'use strict';
// lib/registry/architecture-generator.js — Living Architecture Layer.
//
// Generates YAML files under architecture/ from the current Registry state.
// Triggered on every StateVersion bump (debounced 800ms to batch rapid changes).
// Files in architecture/ are NEVER hand-edited — Registry owns them entirely.
// Git history of architecture/ is the history of how the civilisation understood itself.

const fs   = require('fs');
const path = require('path');

const { EventBus, EVENTS }   = require('./events');
const ARCH_ROOT = path.join(__dirname, '../../architecture');

// ── YAML template helpers ─────────────────────────────────────────────────────

function yamlStr(val) {
    if (val === null || val === undefined) return 'null';
    const s = String(val);
    return s.includes(':') || s.includes('#') || s.includes('"') ? `"${s.replace(/"/g, '\\"')}"` : s;
}

function yamlList(arr, indent = '  ') {
    if (!arr || arr.length === 0) return '  []';
    return arr.map(v => `${indent}- ${yamlStr(v)}`).join('\n');
}

function header(description) {
    return [
        `# Auto-generated by Registry ArchitectureGenerator — DO NOT EDIT`,
        `# ${description}`,
        `# Generated: ${new Date().toISOString()}`,
        '',
    ].join('\n');
}

// ── Generators ────────────────────────────────────────────────────────────────

function generateDomainNode(domain, ctx) {
    const engine       = ctx.engine;
    const healthModule = require('./health-score');

    const entityCount = engine.all().filter(e => {
        // Count entities whose _domain matches or name-match
        return true; // all for now — we'll add domain filtering when ownership settles
    }).length;

    const health = healthModule.compute(domain) || { score: 0, label: 'UNKNOWN', confidence: 0 };

    return [
        header(`Domain node: ${domain.name}`),
        `id: ${domain.id}`,
        `name: ${yamlStr(domain.name)}`,
        `family: DOMAIN`,
        `criticality: ${yamlStr(domain.criticality)}`,
        `status: ${yamlStr(domain.status)}`,
        `owner: ${yamlStr(domain.owner)}`,
        `description: ${yamlStr(domain.description)}`,
        `health:`,
        `  score: ${health.score}`,
        `  label: ${yamlStr(health.label)}`,
        `  confidence: ${health.confidence}`,
        `state_version: ${require('./state-version').StateVersion.current()}`,
        '',
    ].join('\n');
}

function generateCapabilityNode(cap, ctx) {
    const capabilities = ctx.capabilities;
    const status       = capabilities.statusOf ? capabilities.statusOf(cap._cap_key) : null;
    const sv           = require('./state-version').StateVersion.current();

    return [
        header(`Capability node: ${cap.name}`),
        `id: ${cap.id}`,
        `name: ${yamlStr(cap.name)}`,
        `family: CAPABILITY`,
        `criticality: ${yamlStr(cap.criticality)}`,
        `status: ${yamlStr(cap.status)}`,
        `owner: ${yamlStr(cap.owner)}`,
        `description: ${yamlStr(cap.description)}`,
        `state_version: ${sv}`,
        '',
    ].join('\n');
}

function generateAgentNode(agent) {
    const sv = require('./state-version').StateVersion.current();
    return [
        header(`Agent node: ${agent.name}`),
        `id: ${agent.id}`,
        `name: ${yamlStr(agent.name)}`,
        `family: AGENT`,
        `criticality: ${yamlStr(agent.criticality)}`,
        `status: ${yamlStr(agent.status)}`,
        `owner: ${yamlStr(agent.owner)}`,
        `purpose: ${yamlStr(agent.purpose)}`,
        `domain: ${yamlStr(agent._domain || '')}`,
        `state_version: ${sv}`,
        '',
    ].join('\n');
}

function generateServiceNode(svc) {
    const sv = require('./state-version').StateVersion.current();
    return [
        header(`Service node: ${svc.name}`),
        `id: ${svc.id}`,
        `name: ${yamlStr(svc.name)}`,
        `family: SERVICE`,
        `criticality: ${yamlStr(svc.criticality)}`,
        `status: ${yamlStr(svc.status)}`,
        `owner: ${yamlStr(svc.owner)}`,
        `description: ${yamlStr(svc.description)}`,
        `env_var: ${yamlStr(svc._env_var || '')}`,
        `state_version: ${sv}`,
        '',
    ].join('\n');
}

function generateIndex(engine) {
    const all    = engine.all();
    const sv     = require('./state-version').StateVersion.current();
    const counts = {};
    for (const e of all) counts[e.family] = (counts[e.family] || 0) + 1;

    const lines = [
        header('Civilisation architecture index'),
        `state_version: ${sv}`,
        `total_entities: ${all.length}`,
        `entity_families:`,
        ...Object.entries(counts).map(([f, n]) => `  ${f}: ${n}`),
        ``,
    ];
    return lines.join('\n');
}

// ── Write helpers ─────────────────────────────────────────────────────────────

function ensureDir(p) {
    if (!fs.existsSync(p)) fs.mkdirSync(p, { recursive: true });
}

function write(filePath, content) {
    ensureDir(path.dirname(filePath));
    fs.writeFileSync(filePath, content, 'utf8');
}

// ── Main generation pass ──────────────────────────────────────────────────────

function generate() {
    try {
        const engine = require('./engine');
        const ctx    = require('./context');
        const all    = engine.all();

        // architecture/index.yaml
        write(path.join(ARCH_ROOT, 'index.yaml'), generateIndex(engine));

        // architecture/nodes/{id}.yaml for each synthetic universe entity
        const nodesDir = path.join(ARCH_ROOT, 'nodes');
        for (const entity of all) {
            let content = null;
            if (entity.family === 'DOMAIN')      content = generateDomainNode(entity, ctx);
            if (entity.family === 'CAPABILITY')  content = generateCapabilityNode(entity, ctx);
            if (entity.family === 'AGENT')        content = generateAgentNode(entity);
            if (entity.family === 'SERVICE')      content = generateServiceNode(entity);
            if (content) write(path.join(nodesDir, `${entity.id}.yaml`), content);
        }

        EventBus.emit(EVENTS.ARCHITECTURE_UPDATED, { state_version: require('./state-version').StateVersion.current() });
    } catch (err) {
        // Architecture generation is non-fatal — never crash the server
        console.warn('[ArchitectureGenerator] generation failed:', err.message);
    }
}

// ── Debounced trigger ─────────────────────────────────────────────────────────

let _timer = null;

function scheduleGeneration() {
    if (_timer) clearTimeout(_timer);
    _timer = setTimeout(() => { _timer = null; generate(); }, 800);
}

// ── Initialise ────────────────────────────────────────────────────────────────

let _initialised = false;

function init() {
    if (_initialised) return;
    _initialised = true;

    // Trigger on any mutation event
    const MUTATION_EVENTS = [
        EVENTS.ENTITY_CREATED, EVENTS.ENTITY_UPDATED,
        EVENTS.EDGE_ADDED, EVENTS.EDGE_REMOVED,
    ];
    for (const ev of MUTATION_EVENTS) {
        EventBus.on(ev, scheduleGeneration);
    }

    // Initial generation at startup (delayed to let engine warm up)
    setTimeout(generate, 1500);
}

module.exports = { init, generate };
